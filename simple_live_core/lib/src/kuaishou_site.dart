      api,
      queryParameters: {
        "filterType": 0,
        "pageSize": 20,
        "gameId": category.id,
        "page": page,
      },
      header: _headers,
    );

    var items = <LiveRoomItem>[];
    for (var item in result["data"]["list"] ?? []) {
      var cover = item['poster']?.toString() ?? '';
      if (cover.isNotEmpty && !_isImage(cover)) {
        cover = '$cover.jpg';
      }
